/**
 * Fired during plugin activation.
 *
 * This class defines all code necessary to run during the plugin's activation.
 *
 * @since      1.0.0
 * @package    OSS_Sitename
 * @subpackage OSS_Sitename/includes
 * @author     Grit & Oyster <code@grit-oyster.co.uk>
 */
class OSS_Sitename_Activator {

	/**
	 * Short Description. (use period)
	 *
	 * Long Description.
	 *
	 * @since    1.0.0
	 */
	public static function activate() {

		if( ! is_plugin_active( 'oystershell-core/oystershell-core.php' ) ) {
			deactivate_plugins( plugin_basename( __FILE__ ) );
            wp_die( __( 'This plugin requires Oystershell Core.', 'plugin-text-domain' ) );
		}

	}

}
